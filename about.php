<h1>About NBA</h1>
<p>
The National Basketball Association (NBA) is the pre-eminent men's professional basketball league in North America,
and is widely considered to be the premier men's professional basketball league in the world. It has 30 teams
(29 in the United States and 1 in Canada), and is an active member of USA Basketball (USAB), which is recognized by FIBA
(also known as the International Basketball Federation) as the national governing body for basketball in the United States.
The NBA is one of the four major professional sports leagues in the United States and Canada. NBA players are the world's
best paid sportsmen, by average annual salary per player.</p>
<p>
The league was founded in New York City on June 6, 1946, as the Basketball Association of America (BAA).
The league adopted the name National Basketball Association on August 3, 1949, after merging with its rival
 National Basketball League (NBL). The league's several international as well as individual team offices are
 directed out of its head offices located in the Olympic Tower at 645 Fifth Avenue in New York City. 
NBA Entertainment and NBA TV studios are directed out of offices located in Secaucus, New Jersey.
</p>
